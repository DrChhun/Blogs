    public function login(Request $request) {
        // dd($request->all());
        $pass = $request->validate([
            'email' => 'required',
            'password' => 'required'
        ]);

        if(Auth::guard('admin')->attempt($pass)) {

            $request->session()->regenerate();

            return redirect()->to('/main/overview');

        }

        return redirect()->to('/fail');
    }

